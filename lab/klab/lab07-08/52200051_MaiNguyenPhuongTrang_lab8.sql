CREATE PROC CAU2 @Hosv nvarchar(20), @Tensv nvarchar(15), @Masv char(10), @Ngaysinh Date, @Phai char(5), @Makhoa char(10)
AS
BEGIN
	IF NOT EXISTS ( SELECT Makhoa FROM KHOA WHERE Makhoa = @Makhoa)
	PRINT 'NOT EXISTS IN KHOA'
	ELSE IF EXISTS ( SELECT MASV FROM SINHVIEN WHERE MASV = @Masv)
	PRINT'EXISTS IN SINHVIEN'
	ELSE IF (((YEAR(GETDATE()) - YEAR(@Ngaysinh)) < 18 OR (YEAR(GETDATE()) - YEAR(@Ngaysinh)) >= 40))
