CREATE PROC CAU3 ( 
	@MASV CHAR(10), 
	@MAMH CHAR(10), 
	@LANTHI INT, 
	@DIEM INT
)
AS
BEGIN
	DECLARE @MASVTONTAI INT
	SET @MASVTONTAI = 
	(
		SELECT COUNT(*)
		FROM SINHVIEN
		WHERE MASV = @MASV
	)
	IF @MASVTONTAI > 0
	BEGIN
		RAISERROR(N'MASV da ton tai', 16, 1)
		RETURN
	END

	DECLARE @MAMHTONTAI INT
	SET @MAMHTONTAI = 
	(
		SELECT COUNT(*)
		FROM MONHOC
		WHERE MAMH = @MAMH
	)
	IF @MAMH > 0
	BEGIN
		RAISERROR(N'MAMH da ton tai', 16, 1)
		RETURN
	END
END
GO
